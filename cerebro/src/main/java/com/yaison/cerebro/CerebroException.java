package com.yaison.cerebro;

public class CerebroException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8586604681399744956L;

	public CerebroException() {
		super();
	}

	public CerebroException(String message, Throwable cause) {
		super(message, cause);
	}

	public CerebroException(String message) {
		super(message);
	}
	
	

}
